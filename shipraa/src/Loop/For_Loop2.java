package Loop;

public class For_Loop2
{
   public static void main(String[] args)
   {
	 // 1000 to 1
	   
	   for(int i= 1000; i>=1;i--)
	   {
		   System.out.println(i);
	   }
	   
	   System.out.println("==============================");
	   
	   for(int j=99; j>=1;j--)
	   {
		   System.out.println(j);
	   }
	   
	   System.out.println("=========================== ===");
	   
	   for(char ch='A'; ch<='Z';ch++)
	   {
		   System.out.println(ch);
	   }
}
}
