package Loop;

public class For_Loop4 
{
   public static void main(String[] args)  
   {
	 // 6 to 10 table
	   
	   for(int i=6;i<=60; i=i+6) 
	   {
		   System.out.println(i);
	   }
	   
	   System.out.println("=======");
	   
	   for(int i=7;i<=70;i=i+7)
	   {
		   System.out.println(i);
	   }
	   
	   System.out.println("========");
	   
	   for(int i=8;i<=80;i=i+8)
	   {
		   System.out.println(i);
	   }
	   
	   System.out.println("========");
	   
	   for(int i=9;i<=90;i=i+9)
	   {
		   System.out.println(i);
	   }
	   
	   System.out.println("=======");
	   
	   for(int i=10; i<=100;i=i+10)
	   {
		   System.out.println(i);
	   }
   
}
}
