package Loop;

public class While_Loop1 
{
   public static void main(String[] args)  
   {
	   //11 to 111
	  int i=11;
	  int j=1200;
	  while(i<=111)
	  {
		  System.out.println(i);
		  
		  i++;
	  }
	  
	  System.out.println("========");
	  
	  while(j>=1) 
	  {
		  System.out.println(j);
		  
		  j--;
	  }
	  System.out.println("=======");
	  
	  char ch='a';
	  
	  while(ch<='z')
	  {
		  System.out.println(ch);
		  ch++;
	  }
}
}
