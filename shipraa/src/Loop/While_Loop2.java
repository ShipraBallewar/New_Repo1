package Loop;

public class While_Loop2  
{
     public static void main(String[] args)
     {
		//11 to 15 tables
    	 
    	int i=11;
    	int j=12;
    	int k=13;
    	int l=14;
    	int m=15;
    	
    	while(i<=110)
    	{
    		System.out.println(i);
    		i=i+11;
    	}
    	System.out.println("=======");
    	while(j<=120)
    	{
    		System.out.println(j);
    		j=j+12;
    	}
    	System.out.println("========");
    	while(k<=130)
    	{
    		System.out.println(k);
    		k=k+13;
    	}
    	System.out.println("=======");
    	while(l<=140)
    	{
    		System.out.println(l);
    		l=l+14;
    	}
    	System.out.println("=======");
    	while(m<=150)
    	{
    		System.out.println(m);
    		m=m+15;
    	}
    		
    	
	}
}
