package Loop;

public class For_Loop3
{
  public static void main(String[] args)
  {
	 // 2 to 5 table
	  
	  for(int i=2; i<=20;i=i+2)
	  {
		  System.out.println(i);
	  }
	  
	  System.out.println("==========");
	  
	  for(int i=3;i<=30;i=i+3)
	  {
		  System.out.println(i);
	  }
	  
	  System.out.println("==========");
	  
	  for(int i=4;i<=40;i=i+4) 
	  {
		  System.out.println(i);
	  }
	  
	  System.out.println("==========");
	  
	  for(int i=5; i<=50;i=i+5)
	  {
		  System.out.println(i);
	  }
}
}
