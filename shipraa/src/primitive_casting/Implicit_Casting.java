package primitive_casting;

public class Implicit_Casting 
{
   //converting lower data type into higher data type
	
 public static void main(String[] args)
 {
	 int a=52;                //4 byte numeric data type
	 
	 System.out.println("value of interger a=" +a);
	 
	 double b=a;             // 8 byte  decimal data type
	 
	 System.out.println("value of a=" +b);
}
	
}
